/**
 * 获取所有真实新闻
 */
export const getRealMusicNews = () => {
  return REAL_MUSIC_NEWS.sort((a, b) => 
    new Date(b.publishedAt) - new Date(a.publishedAt)
  );
};

/**
 * 按分类获取新闻
 */
export const getNewsByCategory = (category) => {
  if (category === 'all') return getRealMusicNews();
  return REAL_MUSIC_NEWS.filter(news => news.category === category);
};

/**
 * 搜索新闻
 */
export const searchRealNews = (query) => {
  const lowerQuery = query.toLowerCase();
  return REAL_MUSIC_NEWS.filter(news => 
    news.title.toLowerCase().includes(lowerQuery) ||
    news.summary.toLowerCase().includes(lowerQuery) ||
    news.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};

/**
 * 获取最新新闻（指定数量）
 */
export const getLatestNews = (count = 10) => {
  return getRealMusicNews().slice(0, count);
};
